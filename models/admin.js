const mongoose = require("mongoose");
const { Schema, model } = mongoose;

const adminSchema = new Schema({
  nom: {
    type: String,
    required: true,
  },
  prenom: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

module.exports = Admin = model("admin", adminSchema);
